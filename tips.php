    <main class="container">
        <section class="tips-section">
            <div class="tips-intro card">
                <h2><i class="fas fa-lightbulb"></i> Como Melhorar seu Ritmo</h2>
                <p>Melhorar seu pace de corrida requer uma abordagem metódica e consistente. As dicas a seguir foram selecionadas com base em evidências científicas e experiência prática de treinadores e atletas.</p>
                <p>Lembre-se que a progressão deve ser gradual para evitar lesões e permitir que seu corpo se adapte adequadamente.</p>
            </div>
            
            <div class="tips-grid">
                <div class="tip-card">
                    <div class="tip-header">
                        <div class="tip-icon"><i class="fas fa-running"></i></div>
                        <h3>Treinos Intervalados</h3>
                    </div>
                    <div class="tip-content">
                        <p>Alterne entre períodos de alta intensidade e recuperação para desenvolver tanto sua potência quanto resistência.</p>
                        
                        <div class="tip-example">
                            <h4>Exemplo de treino:</h4>
                            <ul>
                                <li>Aquecimento: 10 minutos de corrida leve</li>
                                <li>8-10 repetições de 400m em ritmo forte</li>
                                <li>200m de recuperação entre cada repetição</li>
                                <li>Volta à calma: 10 minutos de corrida leve</li>
                            </ul>
                        </div>
                        
                        <div class="tip-benefits">
                            <h4>Benefícios:</h4>
                            <ul>
                                <li>Aumenta o VO2max (consumo máximo de oxigênio)</li>
                                <li>Melhora a tolerância ao lactato</li>
                                <li>Desenvolve força muscular específica</li>
                            </ul>
                        </div>
                    </div>
                </div>
                
                <div class="tip-card">
                    <div class="tip-header">
                        <div class="tip-icon"><i class="fas fa-heartbeat"></i></div>
                        <h3>Treinamento em Zonas</h3>
                    </div>
                    <div class="tip-content">
                        <p>Estruture seus treinos com base nas zonas de frequência cardíaca para otimizar cada sessão de acordo com seu objetivo específico.</p>
                        
                        <div class="tip-example">
                            <h4>Distribuição recomendada:</h4>
                            <ul>
                                <li>80% dos treinos em intensidade baixa (Zonas 1-2)</li>
                                <li>20% dos treinos em intensidade alta (Zonas 4-5)</li>
                                <li>Evite a "zona cinzenta" (Zona 3) constantemente</li>
                            </ul>
                        </div>
                        
                        <div class="tip-benefits">
                            <h4>Benefícios:</h4>
                            <ul>
                                <li>Evita sobrecarga e burnout</li>
                                <li>Maximiza adaptações específicas</li>
                                <li>Permite recuperação adequada</li>
                            </ul>
                        </div>
                    </div>
                </div>
                
                <div class="tip-card">
                    <div class="tip-header">
                        <div class="tip-icon"><i class="fas fa-dumbbell"></i></div>
                        <h3>Fortalecimento</h3>
                    </div>
                    <div class="tip-content">
                        <p>Incorpore treinamento de força na sua rotina para melhorar a economia de corrida e prevenir lesões.</p>
                        
                        <div class="tip-example">
                            <h4>Exercícios recomendados:</h4>
                            <ul>
                                <li>Agachamentos e variações</li>
                                <li>Exercícios para o core (prancha, bird-dog)</li>
                                <li>Exercícios unilaterais (afundo, agachamento búlgaro)</li>
                                <li>Exercícios de estabilidade para tornozelo e quadril</li>
                            </ul>
                        </div>
                        
                        <div class="tip-benefits">
                            <h4>Benefícios:</h4>
                            <ul>
                                <li>Melhora a economia de corrida</li>
                                <li>Aumenta a potência</li>
                                <li>Reduz o risco de lesões</li>
                            </ul>
                        </div>
                    </div>
                </div>
                
                <div class="tip-card">
                    <div class="tip-header">
                        <div class="tip-icon"><i class="fas fa-stopwatch"></i></div>
                        <h3>Treinos de Ritmo</h3>
                    </div>
                    <div class="tip-content">
                        <p>Pratique manter um pace específico por distâncias progressivamente maiores para "ensinar" seu corpo a sustentar o ritmo desejado.</p>
                        
                        <div class="tip-example">
                            <h4>Exemplo de treino:</h4>
                            <ul>
                                <li>Aquecimento: 10-15 minutos</li>
                                <li>5-8 km no ritmo da próxima prova alvo</li>
                                <li>Volta à calma: 10 minutos</li>
                            </ul>
                        </div>
                        
                        <div class="tip-benefits">
                            <h4>Benefícios:</h4>
                            <ul>
                                <li>Desenvolve percepção de esforço</li>
                                <li>Melhora confiança na manutenção do ritmo</li>
                                <li>Prepara mental e fisicamente para provas</li>
                            </ul>
                        </div>
                    </div>
                </div>
                
                <div class="tip-card">
                    <div class="tip-header">
                        <div class="tip-icon"><i class="fas fa-mountain"></i></div>
                        <h3>Treinamento em Ladeiras</h3>
                    </div>
                    <div class="tip-content">
                        <p>Incorpore subidas na sua rotina para desenvolver força, potência e resistência específicas.</p>
                        
                        <div class="tip-example">
                            <h4>Exemplo de treino:</h4>
                            <ul>
                                <li>Aquecimento: 10 minutos em terreno plano</li>
                                <li>8-10 subidas de 100-200m em intensidade moderada-alta</li>
                                <li>Recuperação: desça a ladeira caminhando/trotando</li>
                                <li>Volta à calma: 10 minutos em terreno plano</li>
                            </ul>
                        </div>
                        
                        <div class="tip-benefits">
                            <h4>Benefícios:</h4>
                            <ul>
                                <li>Desenvolve força específica para corrida</li>
                                <li>Melhora técnica e postura</li>
                                <li>Aumenta resistência muscular</li>
                            </ul>
                        </div>
                    </div>
                </div>
                
                <div class="tip-card">
                    <div class="tip-header">
                        <div class="tip-icon"><i class="fas fa-bed"></i></div>
                        <h3>Recuperação</h3>
                    </div>
                    <div class="tip-content">
                        <p>Nunca subestime a importância do descanso e recuperação em seu plano de treinamento.</p>
                        
                        <div class="tip-example">
                            <h4>Estratégias eficazes:</h4>
                            <ul>
                                <li>7-9 horas de sono por noite</li>
                                <li>Dias de descanso completo no planejamento semanal</li>
                                <li>Corridas de recuperação realmente lentas</li>
                                <li>Técnicas como massagem, foam roller, crioterapia</li>
                            </ul>
                        </div>
                        
                        <div class="tip-benefits">
                            <h4>Benefícios:</h4>
                            <ul>
                                <li>Permite adaptações fisiológicas</li>
                                <li>Previne overtraining e lesões</li>
                                <li>Mantém motivação e saúde mental</li>
                            </ul>
                        </div>
                    </div>
                </div>
            </div>
